package com.example.animaladoptionapp;

public class UserCodClass {

    String idCOD, nameCOD, phoneCOD, addressCOD, timeCOD, namePet, sexPet, agePet, breedPet, conditionPet;

    public UserCodClass() {
    }

    public UserCodClass(String idCOD, String nameCOD, String phoneCOD, String addressCOD, String timeCOD, String namePet, String sexPet, String agePet, String breedPet, String conditionPet) {
        this.idCOD = idCOD;
        this.nameCOD = nameCOD;
        this.phoneCOD = phoneCOD;
        this.addressCOD = addressCOD;
        this.timeCOD = timeCOD;
        this.namePet = namePet;
        this.sexPet = sexPet;
        this.agePet = agePet;
        this.breedPet = breedPet;
        this.conditionPet = conditionPet;
    }

    public String getIdCOD() {
        return idCOD;
    }

    public void setIdCOD(String idCOD) {
        this.idCOD = idCOD;
    }

    public String getNameCOD() {
        return nameCOD;
    }

    public void setNameCOD(String nameCOD) {
        this.nameCOD = nameCOD;
    }

    public String getPhoneCOD() {
        return phoneCOD;
    }

    public void setPhoneCOD(String phoneCOD) {
        this.phoneCOD = phoneCOD;
    }

    public String getAddressCOD() {
        return addressCOD;
    }

    public void setAddressCOD(String addressCOD) {
        this.addressCOD = addressCOD;
    }

    public String getTimeCOD() {
        return timeCOD;
    }

    public void setTimeCOD(String timeCOD) {
        this.timeCOD = timeCOD;
    }

    public String getNamePet() {
        return namePet;
    }

    public void setNamePet(String namePet) {
        this.namePet = namePet;
    }

    public String getSexPet() {
        return sexPet;
    }

    public void setSexPet(String sexPet) {
        this.sexPet = sexPet;
    }

    public String getAgePet() {
        return agePet;
    }

    public void setAgePet(String agePet) {
        this.agePet = agePet;
    }

    public String getBreedPet() {
        return breedPet;
    }

    public void setBreedPet(String breedPet) {
        this.breedPet = breedPet;
    }

    public String getConditionPet() {
        return conditionPet;
    }

    public void setConditionPet(String conditionPet) {
        this.conditionPet = conditionPet;
    }
}
